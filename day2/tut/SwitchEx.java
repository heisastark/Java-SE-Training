package day2.tut;

public class SwitchEx {

	public static void main(String[] args) {
		
		switch(1) {
		
		case 1:
			
		case 10:
			System.out.println("One"); break;
		case 2:
			System.out.println("Two");break;
		case 3:
			System.out.println("three");break;
		
		default :
			System.out.println("Neither Matches");
			

		}

	}

}


// 1  (or) 10 ===> one