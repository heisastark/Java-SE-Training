package day2.tut;

public class whileEx1 {

	public static void main(String[] args) {

		int i=10;
		while(i<5) {
			System.out.println("Hello");
			System.out.println("welcome");
			i++;
		}
		System.out.println("=============");
		do{
			System.out.println("Hello");
			System.out.println("welcome");
			i++;
		}while(i<5);

	}

}
